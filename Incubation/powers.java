import java.util.*;
class powers{
public static void main(String[] args){
Scanner sc=new Scanner(System.in);
System.out.print("Input:");
int n=sc.nextInt();
int s=n*n;
int c=n*n*n;
int f=n*n*n*n;
System.out.println("Square:"+s);
System.out.println("Cube:"+c);
System.out.println("Fourth power:"+f);

}
}
